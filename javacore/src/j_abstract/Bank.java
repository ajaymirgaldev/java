package j_abstract;

// Abstract Class //
public abstract class Bank {
	
	// A method which is declared as abstract and does not have implementation is known as an abstract method.
	//The implementation will be performed in child class //
	
	//abstract method workingHrs
	abstract void workingHrs();
	
	//abstract method rateOfInterest //
	abstract float rateOfInterest();
	
	//abstract method average monthly balance //
	abstract int amb();
}
